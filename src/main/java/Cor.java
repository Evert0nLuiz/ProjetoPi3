public class Cor {
    private String nomeDaCor;
    private int id;


    public Cor(String nomeDaCor){
        this.nomeDaCor = nomeDaCor;

    }

    public String getCor() {
        return nomeDaCor;
    }

    public int getId() {
        return id;
    }

    public void setCor(String cor) {
        this. nomeDaCor = cor;
    }

    public void setId(int id) {
        this.id = id;
    }


}
